# Solicitar al usuario que ingrese un número como cadena
numero_str = input("Por favor, ingresa un número: ")

# Convertir la cadena a un número de punto flotante
numero = float(numero_str)

# Multiplicar el número por 2
resultado = numero * 2

# Mostrar el resultado
print("El resultado de multiplicar {} por 2 es: {}".format(numero, resultado))
